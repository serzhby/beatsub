package by.serzh.beatsub.ui;

public class Dimens {
    private final double height;
    private final double width;

    public Dimens(double width, double height) {
        this.width = width;
        this.height = height;
    }

    public double getWidth() {
        return width;
    }

    public double getHeight() {
        return height;
    }
}
